"""
Заповнити двовимірний масив розміром 7x7 таким чином, як показано на рисунку згідно з варіантом. 
Вивести масив на екран. Для виконання завдання використовуйте цикли.
"""

n = 7

a = []
for i in range(n):
    row = []
    for j in range(n):
        row.append(n - abs(i - j)) # Заповнення рядка
    a.append(row) # Додавання рядка до масиву

for r in a:
        print(*r)