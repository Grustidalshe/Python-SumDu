# Знайти та роздрукувати ряд Фібоначчі з 5-го по 25-й елемент та підрахувати їх кількість

fib = [1, 1]
while len(fib) < 25:
    fib.append(fib[-1] + fib[-2]) # fib[-1] — останній елемент списку 
                                  # fib[-2] — передостанній елемент

fibs = fib[4:25]  # 5-й - 25-й (індекс 4 - 24) 25 не потрапляє в діапазон
for idx, val in enumerate(fibs, start=5):
    print(f"{val}")

print("Кількість чисел:", len(fibs))

